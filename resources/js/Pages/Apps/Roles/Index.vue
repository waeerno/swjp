<template>

    <div class="row" id="table-hover-animation">
        <div class="col-12">
            <div class="card">
                <div class="card-header">
                    <h4 class="card-title">Roles</h4>
                </div>
                <div class="card-body">
                    <form @submit.prevent="handleSearch">
                        <div class="input-group mb-3">
                            <Link href="/apps/roles/create" v-if="hasAnyPermission(['roles.create'])"
                                class="btn btn-primary input-group-text"> <i class="fa fa-plus-circle me-2"></i> NEW
                            </Link>
                            <input type="text" class="form-control" v-model="search"
                                placeholder="search by role name...">
                            <button class="btn btn-primary input-group-text" type="submit">
                                <i data-feather='search'></i>
                            </button>
                        </div>
                    </form>
                </div>
                <div class="table-responsive">
                    <table class="table table-hover-animation">
                        <thead>
                            <tr>
                                <th>No</th>
                                <th scope="col">Role Name</th>
                                <th scope="col" style="width:50%">Permissions</th>
                                <th scope="col" style="width:20%">Actions</th>
                            </tr>
                        </thead>
                        <tbody>
                            <tr v-for="(role, index) in roles.data" :key="index">
                                <td>{{ index +1 }}</td>
                                <td>{{ role.name }}</td>
                                <td>
                                    <span v-for="(permission, index) in role.permissions" :key="index"
                                        class="badge badge-primary shadow border-0 ms-2 mb-2">
                                        {{ permission.name }}
                                    </span>
                                </td>
                                <td class="text-center">
                                    <Link :href="`/apps/roles/${role.id}/edit`" v-if="hasAnyPermission(['roles.edit'])"
                                        class="btn btn-success btn-sm me-2"><i class="fa fa-pencil-alt me-1"></i> EDIT
                                    </Link>
                                    <button @click.prevent="destroy(role.id)" v-if="hasAnyPermission(['roles.delete'])"
                                        class="btn btn-danger btn-sm"><i class="fa fa-trash"></i> DELETE</button>
                                </td>
                            </tr>
                        </tbody>
                    </table>
                    <Pagination :links="roles.links" align="end" />
                </div>
            </div>
        </div>
    </div>
</template>

<script>
//import layout
import LayoutApp from '../../../Layouts/App.vue';
//import component pagination
import Pagination from '../../../Components/Pagination.vue';
//import Heade and Link from Inertia
import { Head, Link } from '@inertiajs/inertia-vue3';
//import ref from vue
import { ref } from 'vue';
//import inertia adapter
import { Inertia } from '@inertiajs/inertia';

//import sweet alert2
import Swal from 'sweetalert2';

export default {
    //layout
    layout: LayoutApp,
    //register component
    components: {
        Head,
        Link,
        Pagination
    },
    props: {
        roles: Object,
    },
    setup() {
        //define state search
        const search = ref('' || (new URL(document.location)).searchParams.get('q'));
        //define method search
        const handleSearch = () => {
            Inertia.get('/apps/roles', {
                //send params "q" with value from state "search"
                q: search.value,
            });
        }

        //define method destroy
        const destroy = (id) => {
            Swal.fire({
                title: 'Are you sure?',
                text: "You won't be able to revert this!",
                icon: 'warning',
                showCancelButton: true,
                confirmButtonColor: '#3085d6',
                cancelButtonColor: '#d33',
                confirmButtonText: 'Yes, delete it!'
            })
                .then((result) => {
                    if (result.isConfirmed) {
                        Inertia.delete(`/apps/roles/${id}`);
                        Swal.fire({
                            title: 'Deleted!',
                            text: 'Role deleted successfully.',
                            icon: 'success',
                            timer: 2000,
                            showConfirmButton: false,
                        });
                    }
                })
        }

        //return
        return {
            search,
            handleSearch,
            destroy
        }
    }
}
</script>

